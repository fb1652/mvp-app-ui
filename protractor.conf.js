const { SpecReporter } = require('jasmine-spec-reporter');

exports.config = {
  allScriptsTimeout: 11000,
  specs: [
    './e2e/**/login.e2e-spec.ts', // must be first to create user accounts if necessary
    './e2e/**/*.e2e-spec.ts'
  ],
  capabilities: {
    'browserName': 'chrome'
  },
  directConnect: true,
  baseUrl: 'http://localhost:4200/',
  framework: 'jasmine',
  jasmineNodeOpts: {
    showColors: true,
    defaultTimeoutInterval: 30000,
    print: function() {}
  },
  params: {},
  onPrepare() {
    require('ts-node').register({
      project: 'e2e/tsconfig.e2e.json'
    });
    jasmine.getEnv().addReporter(new SpecReporter({ spec: { displayStacktrace: true } }));

    // set global config
    browser.get('/');
    return browser.driver.findElement(by.id('config-auth')).getAttribute('value') // hidden config value in html
    .then((result) => browser.params.auth = result) // store
    .then(() => console.log(`Authentication: ${browser.params.auth}`));
  }
};
